var num1 = +prompt ("Enter Value 1")
var num2 = +prompt ("Enter Value 2")
var op = prompt("Enter Operator")

if (op =="+"){
    document.write(num1+num2)
}
else if (op=="-"){
    document.write(num1-num2)
} 
else if (op=="*"){
    document.write(num1*num2)
} 
else if (op=="/"){
    document.write(num1/num2)
} 
else if (op=="%"){
    document.write(num1%num2)
} 


